        <p>Date: 03/15/2022</p>
        <p>Author:  Nitin Umredkar</p>
        <h1>Automation of Design Rule Checks in CATIA V5 for Wire Harness CAD models for Major J-OEM</h1>
        <img src="img/casestudy/Picture3.png" class="cs_main">
        <p>Design Rule Checks which take a huge amount of time for implementation are automated in CATIA with little to no user intervention.</p>
        <p style="font-weight: bold">Executive Summary:</p>
        <p>Design Rule Checks which were implemented manually at the end of completion of Wire Harness CAD models which now can be implemented while designing the CAD models with just one click. This automation was done to achieve following goals:
            •	Save man hours used in implementing laborious and time-consuming checks
            •	Improve quality of CAD models by eliminating human error.
            •	Generate reports to create evidence of passed and failed checks
            •	Checks can be implemented during the deign process to rectify errors and save time.
            </p>

        <p style="font-weight: bold">Challenges and Objectives:</p>
        <p>Implementation of Design Rule Checks requires a huge amount of time and man hours. There are checks where an Engineer has to go through all the parts and products and apply these checks which might result in failure of some components and then the engineer has to go back to design stage and rectify those errors. This might result in delay of further processes. The Wire Harness CAD models are designed in Electrical Harness Assembly and there is little to no scope for Electrical objects automation in CATIA. This was a huge problem going forward as all these checks are implemented on electrical objects. The objective of this project was to identify these laborious checks and automate them in CATIA v5 so that these can be implemented during design stage.  This will result in identification of most of the error beforehand which can be then rectified to prevent delay in further processes.</p>
        
        <p style="font-weight:bold">How Product/Service Helped:</p>
        <p>After completion of the Automation of the Design rule process, reduced the hours required to complete implementation of Checks from hours to just minutes. To achieve the completion of project first the design rule checks were identified and automated using CATIA V5 VBA and EKL languages. The laborious and time-consuming process of implementation of Design rule checks has now evolved from manual process to an automated process which can be done in matter of clicks.</p>
       
        <p style="font-weight:bold">Results:</p>
        <p>This project has completely changed the implementation of Design Rule Checks on CAD models. The conventional method of implementing the checks on each part and assembly has been replaced with an automated process which Is fast, agile and robust. This has helped the OEM to save man hours and decrease the number of errors which helps in expedite the complete process and helps designer to deliver their part on time.</p>

        <p style="font-weight:bold">Supporting Visuals or Quotes:</p>
        <p>“You’re either the one that creates the automation or you’re getting automated” by Tom Preston</p>
    </section>


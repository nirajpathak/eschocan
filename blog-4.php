        <h1>How to Prepare and Accelerate your job Search </h1>
        <img src="img/blog/Picture4.png" class="cs_main">
        <p>Searching for a new job can be a stressful for anyone. Whether you need a job immediately or looking to switch up your career there are ways for you to prepare and accelerate your job search. </p>
        
        <p class="blog_parahead" style="font-weight: bolder;">Where to start</p>
        <p>The first step when beginning your search for a job is an impressive resume that will stand out. Your resume is the first impression you are giving to the employer, so it is key to have a well formatted resume that highlights the most important parts of your career that pertain to the job you are applying for. </p>
        <p>Important tips for a good resume </p>
        <p>•	A resume may need to be adjusted for the different positions you are applying for. Gear your resume to the job description.  
          <br>  •	Include the key information that shows the employer you are qualified to do the job they are hiring for. 
          <br>•	Be clear and to the point when summarizing responsibilities of previous jobs. If you have many years of experience, only include previous experience that is relevant to the job you are applying for. 
          <br>•	The resume should be formatted so it is easy to read and follow
        </p>

        <p class="blog_parahead" style="font-weight: bolder;">Where to look</p>
        <p>After you have an updated resume, it is time to get your resume out there and start applying to jobs. Thankfully with technology and social media today you don’t even need to leave your house to start finding and applying to new jobs. Great websites to find companies that are hiring include, LinkedIn, Indeed, Zip Recruiter, CareerBuilder, etc. Many of these websites will allow you to apply directly through the website, or you can check the company’s website to see if you can on there. </p>
        <p>Once you have applied to a position it is time to be patient. There could be over 100 applicants for that same position. Companies or managers may take some time to review and respond to all the applicants. While waiting to hear back about the position it is important to keep applying for more positions. </p>

        <p class="blog_parahead" style="font-weight: bolder;">Recruiters </p>
        <p>Recruiters can be a great resource when it comes to your job search. A recruiter can provide you more exposure to jobs than you may be able to do on your own. Recruiters may also know about jobs that have not been made available to the public yet. </p>
        <p>A recruiter can also be an advocate for you through the interview/ hiring process</p>


        <h1>ADAS control systems role in the Automotive industry</h1>
        <img src="img/blog/Picture1.png" class="cs_main">
        <p>Automobiles are evolving rapidly, and a driver that takes a few years' break may be overwhelmed by the number of features when they return to the driver's seat! One of the features, which may even help the driver avoid what drivers do, is ADAS (advanced driver-assistance systems). What are ADAS, and what role do ADAS control systems have in the automotive industry? Keep reading to find out.
        </p>

        <p class="blog_parahead">What are ADAS?</p>
        <p>ADAS are technologies that enhance the safety of the occupants of a vehicle when moving on the road. They achieve this by employing a human-machine interface to help the driver better respond to dangers on the road.</p>
        <p>Well-designed ADAS give the driver early warnings of tricky situations, which increases the amount the driver has to react to the threat.</p>
        <p>Some ADAS come preinstalled in some automobile models, although often as an optional upgrade. However, some vendors provide aftermarket solutions for drivers that want to invest in their safety.</p>

        <p class="blog_parahead">What features do ADAS offer?</p>
        <p>ADAS encompass multiple features or technologies that identify particular dangers or signals. For example, ADAS power Adaptive Cruise Control, Anti-lock Brakes, Forward Collision Warning, Traction Control, Lane Departure Warning, High Beam Safety System, Traffic Signals Recognition, etc.</p>
        <p>The earliest ADAS could be traced to the late 1970s when the first electronic anti-lock braking systems were released, although it was a mechanical invention. However, by the early 2000s, ADAS had begun gaining traction stateside. The earliest models with the modern ADAS include the 2000 Cadillac Deville equipped with Night Vision, the 2000 Toyota with Dynamic Laser Cruise Control, the 2004 Infinity FX boasting Lane Departure warning, the 2007 Audi with Lane Assist, etc.</p>
        <p>Regulations have assisted in promoting the adoption of ADAS. For example, the National Highway Traffic Safety Administration, NHTSA, of the United States mandated all new vehicles weighing less than 10,000 pounds (4,500 kg) must be outfitted with a rear-view camera by 2018. This was in response to a tragic event that claimed a child's life when a car backed out of a driveway.</p>
        <p>However, some really clever ADAS have resulted from automakers' ingenuity. For example, GM started adding a vibrating driver's seat with the 2013 Cadillac ATS. The idea is that the seat vibrates when the car begins to drift out of its lane, which could happen when a driver is drowsy.</p>

        <p class="blog_parahead">Why equip automobiles with ADAS?</p>
        <p>ADAS are meant to make up for human errors responsible for most road accidents. These technologies have proven helpful, thereby saving numerous lives and properties.</p>
        <p>By automating the movement of the car, ADAS help to enhance safety. They also alert drivers to take control of the car when danger looms. Some track the driver and raise notifications if it appears they are dozing off.</p>
        <p>The insurance industry directly benefits from ADAS because they reduce claims due to accidents.</p>

        <p class="blog_parahead">How do ADAS work?</p>
        <p>ADAS require special hardware and software. The hardware includes cameras, LiDAR, sensors, computer chips, etc.</p>
        <p>The hardware allows the car to get input about its surrounding in real-time. These inputs include imaging capable of 360-degree views, 3-D, and high visibility even in bad weather and lighting conditions. LiDAR helps distinguish between static and moving obstacles, which can identify blind spots. It also sees more in low-light situations.</p>
        <p>The ADAS then crunches the inputs and uses other technologies like AI to make decisions in real-time, which is then communicated to the driver. The feedback to the driver could come in audio, vibration, or even overlaid on the windscreen, known as a heads-up display.</p>
        <p>Some carmakers equip the car with all the required hardware in the factory, although customers may be able to get aftermarket packages. Many times, the performance of the controlling software is enhanced through over-the-air updates.</p>

        <p class="blog_parahead">What is the future of ADAS in the automotive industry?</p>
        <p>Experts believe ADAS will continue to get better. They expect ADAS to allow cars to communicate with one another on the road using Vehicle to Vehicle (V2V) or Vehicle to Infrastructure (V2I). The communication would take place over Wi-Fi. Such a feature will make the road safer as the cars can coordinate with themselves to yield the safest outcomes.</p>
        <p>One of the most popular examples of ADAS is Tesla's Autopilot, which the company touts as offering Level 2 vehicle automation. This means that even though it can drive itself, the driver still has to be alert to take over control when required. However, the holy grail of autonomous driving is Level 5, which means the car can drive itself without human intervention. The consensus is that Level 5 autonomy is still years away. However, car owners are getting more comfortable with letting their car drive itself. This is supported by <a href="https://www.automotiveworld.com/articles/the-race-to-level-5-will-be-won-through-collaboration/">a Publicis Sapient and Epsilon research</a> a which revealed almost 50 percent of 18 to 34 year old Americans would consider purchasing a self-driving car.</p>
        <p>Another company with sophisticated ADAS is GM, which owns Cruise. Some experts believe the first-generation Super Cruise is the safest in the industry. However, GM has released a newer and more powerful version called Ultra Cruise, which can be used on 2 million miles of paved roads in 95 percent of driving scenarios. The two versions will coexist as the latter will only be offered on the premium models.</p>
        <p>There is no doubt that ADAS is here to stay, as automotive analysts predict the <a href="https://www.globaledgesoft.com/what-is-adas/"> market for ADAS in the Asia-Pacific region will reach $9.69 billion by 2023.</a>
            <br>However, ADAS will have to overcome some challenges before entering the mass market. The first issue is the cost, as ADAS are usually a premium. Even after purchase, ADAS have been studied to add to the cost of repairs.<a href="https://www.tu-auto.com/aaa-adas-systems-could-drive-up-vehicle-repair-costs/"> According to data by the AAA</a>, a typical repair involving the front radar sensor used for automatic emergency braking and adaptive cruise control systems could cost the owner between $900 and $1,300. The rear radar sensors could cost up to $2,050 in repairs.
            </p>
        <p>Another problem is getting the tech right. ADAS, especially when it includes autonomous operation, are hard to achieve and require lots of testing.
            <br>Misuse is another problem facing ADAS. The <a href="https://aaafoundation.org/wp-content/uploads/2018/09/VehicleOwnersExperiencesWithADAS_TechnicalReport.pdf">AAA reported</a> that many motorists are “unaware of the safety limitations” of ADAS, leading to false expectations. As such, ADAS become a hazard instead of a helpful tool.
            </p>


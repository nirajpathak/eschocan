        <h1>Software Platform Development for Connected Boating</h1>
        <p>As the pace innovation continues to accelerate, it’s harder to keep up with today’s new breed of digital-savvy competitors. That’s why one of the world’s leading and most profitable recreational boating and lifestyle brands turned to eShocan to help their executives learn exactly how they could ride the wave of change in the digital age</p>
        <img src="img/casestudy/Picture6.png" class="cs_main">
        <p>The development of a “smart boating platform” is a great example of how one company is investing in connectivity with a digitally enabled product dedicated to the marine environment. By connecting wireless onboard sensors to a central hub, their customers can now track, monitor, and control key components and operating systems remotely with a simple tap on a mobile device.</p>
        <p>In today’s increasingly connected world where the IoT is steadily improving our lives on many fronts, the connected boat makes perfect sense. After all, boater s often live far from their boats – and their boats live in a far more challenging environment than most houses and vehicles.</p>
        <p style="font-weight: 900">Removing Digital Barriers through Innovative IoT Solutions</p>
        <p>It wasn’t that long ago that monitoring solutions for recreational boats were too expensive and difficult to deploy. This has since changed with the emergence of new affordable smart sensors and flexible cloud infrastructure services. While digital innovation offers boundless possibilities for growth and value creation, it comes with its own set of engineering challenges. Cutting through the strategic and technical clutter is a core strength of eShocan. “Connecting our client’s product to the cloud is important; making sure that it connects to their customers and enhances both their brand and their business creates value,” observes Girish Gowda, CTO of eShocan. In many markets, the fight to become the market leader is a competition between mature incumbents and emerging, young startups. In the case of this Smart Boating System, the market leader created a new start-up to more fully capture the value of the innovation across the recreational marine industry.</p>

        <p style="font-weight: bold">The Result:</p>
        <p>an innovative and robust connected platform that provides users with a remote view of the condition of their boat’s key components and operating systems and alerts them when systems are malfunctioning - preventing surprises before they even set sail. Users can share details of their boat’s status with their marine service provider to rapidly resolve any problems. Driven by a simple smartphone application and powerful and secure IoT cloud, the Smart Boating Platform provides full remote monitoring of your boat. Engine status, battery life, bilge-pump activity, location data with bread-crumbing and geofencing - the list goes on. Following installation of the onboard system, the boar owner receives real-time alerts notifying them of every aspect of their boat’s condition.</p>

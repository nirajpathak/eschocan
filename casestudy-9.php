        <p>Date: 03/20/2021</p>
        <p>Author: Arjunshankar Dhanushkodi</p>
        <h1>Implementation of quality check sheet for Engineering Change Request notices.</h1>
        <img src="img/casestudy/Picture9.png" class="cs_main">

        <p>Implement a quality check sheet for the delivery of CAD and Drawing corresponding to the Engineering Change Request notices for Major Automotive OEM in United States.</p>
        <p>Engineering Change Request (ECR) notice is a common process that almost all parts undergo during their lifecycle. A single part can undergo multiple ECR’s during its lifecycle. There were multiple cases where each ECR was handled by different Engineer based on their availability The main highlights of the check sheet are listed below.</p>

        <p>•	The check sheet had classification based on complexity of changes and number of parts involved in each ECR.
        <br>•	The Engineer has to follow the check sheet and mark their initials on the check sheet against each and point and submit it along with the data.
        <br>•	Doing this made it easy for the Engineers to understand the history of the changes made to the part and modify the data easily.
        <br>•	The time required to process each ECR reduced by 30% and quality of CAD and drawing increased by 50%.</p>

        <p style="font-weight: bold;">Challenges and Objectives:</p>
        <p>Since each person had different style of handling the CAD and drawing there was no consistency between the changes and the quality of CAD and drawing kept going down when each Engineer handled the changes.</p>

        <p>This in turn caused confusion and made the lead time for implementation of ECR changes longer and harder to track. So the customer wanted to improve the quality and have a common process across the entire organization we implemented a quality check sheet which had checks to ensure the changes were made to CAD and drawing following a particular process and check points which made anyone working on the parts easy to understand and modify the parts later.</p>
        <p>Implementation of a single process across different which had different methods of working was a challenge. The check sheet had to robust and fool proof with scope to add improvements at a later stage if required.</p>

        <p style="font-weight: bold;">How Product/Service Helped:</p>
        <p>We created a check sheet template that was in a excel format which each engineer working on a ECR can retrieve from a common database accessible to all engineer’s working on the project. This check sheet was very comprehensive and was made to cater each department based on the type of ECR’s they receive. The check sheet was further classified based on the complexity of the task, type of change being made, number of parts involved and if changes are being made to drawing or CAD alone.</p>
        <p>Implementation of this check sheet created a streamlined process across the organization and reduced efforts wasted in understanding the history and rework. Since the check sheet was completely online it could be retrieved anytime by just a click of a button.</p>
        <p>Engineers were able to track each part undergoing changes which were part of the ECR and the check sheet ensured changes required on all parts are being done without missing any parts while delivery.</p>


        <h1>SERVICE COST REDUCTION</h1>
        <p style="font-weight: bold">Background: - </p>
        <p>Aero engine manufacturer typical enter maintenance contract with Airlines based on a fixed serving cost per flying hour in TBO (Time Between overhaul). The revenue generation ability of the OEM is directly proportionate to the airline flying hours.</p>
        <img src="img/casestudy/Picture2.png" class="cs_main">
        <p style="font-weight: bold">The opportunity: -</p>
        <p>The need for reducing the maintenance cost for the entire lifecycle of an aeroengine was need of the hour. The cost saving project was launched under the direct supervision of the top management since it has a direct impact on the bottom line of the company</p>
        <p style="font-weight:bold">Challenges: -</p>
        <p>The major service cost driver is replacement of rejected parts and the associated man hour cost in the shop floor. More often the parts are rejected due to the low damage tolerance that was set up during EIS (Entry into Service) and also with the fact that there will be no service history of the parts. </p>
        <img src="img/casestudy/2_1.png" alt="" class="fig_img">

        <p style="font-weight:bold">Solution: -</p>
        <p>Increasing the acceptable damage limit on parts will increase its life span on the engine thereby reducing the cost. This seems quite easy but the entire process involves lot of deliberation, technical feasibility study, approval by regulatory agencies and validation in shops not losing sight on the whole business benefit</p>
        <img src="img/casestudy/2_2.png" alt="" class="fig_img">

        <p style="font-weight:bold">Result: -</p>
        <p>During the first year of the launch, the team has developed technical solutions for more than 30+ components along with showcasing the business case benefit of close to 120 Mn USD Total Term Cost Savings (TTCS) for the OEM. This is 10% more than the target set for the year. </p>
        <img src="img/casestudy/2_3.png" alt="" class="fig_img">
